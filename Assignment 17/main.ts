// storing 5 places name around the world in a array

let places : string[] = ["MinarePakistan", "MasjidNabvi", "Roserasool", "Haram", "Istanbul"];

// printing the array in its original form

console.log("Array In Original Order:", places);

// now printing the array in alphabatical order without modifying the actual list

console.log("Array In Alphabatical Order:", [...places].sort());

// printing the array in original order

console.log("Array Still In Original Odrer:", places);

// printing the array in reverse alphabatical order

console.log("Array In Reverse Alphabatical Order:", [...places].reverse());

// printing the array in original order

console.log("Array Still In Original Order:", places);

// reverse the order of the original list

console.log("The Original List Reversed:", places.reverse());

// again reverse the order of original list

console.log("The Original list Back To Its Original Order:", places.reverse());

// sort the original array

console.log("The Original List Sorted:", places.sort());

// again reversed the original list 

console.log("The Original List Reversed:", places.reverse());

